<?php
function calculateArea($width1, $height1) {
    $area = $width1 * $height1;
    return $area;
}

// Example usage:
$width1 = 4;
$height1 = 2;

$result = calculateArea($width1, $height1);

echo "The area of a rectangle with a width of $width1 and height of $height1 is $result";
?>

